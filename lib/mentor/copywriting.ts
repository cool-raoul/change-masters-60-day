export const STORY_POST_FRAMEWORK = `
STORY-POST-OPBOUW (7 stappen, in deze volgorde, als doorlopend verhaal zonder kopjes):
1. HOOK: overtuiging-kantelende openingszin, kwetsbaar of verrassend. Nooit "wauw wat een verschil" (te tam, dat post iedereen al). Wel: iets wat een aanname omverduwt ("Iedereen zei: dat hoort erbij. Ik heb het bijna geloofd.").
2. WORSTELING: hoe het was, met minstens twee ZINTUIGLIJKE details (tijdstip, plek, een concreet moment: "elke middag rond drie uur", "op het verjaardagsfeestje zat ik erbij maar was ik er niet").
3. ANGST: wat er echt op het spel stond, de gedachte die 's nachts wakker hield. Menselijk, geen drama om het drama.
4. ONTDEKKING: hoe diegene het antwoord vond, via een mens, niet via een product. Sla dit niet over, dit is het herkenbare deel.
5. HARDE WAARHEID: "ik dacht dat het aan X lag... het bleek voor mij in Y te zitten." Het aha-moment dat vertrouwen verdient. Y is altijd gedrag/leefstijl, nooit een product.
6. RESULTAAT + DROOM: concreet voor-en-na in gevoel en gedrag (claim-vrij), plus wat nu kan wat eerst niet kon, het liefst als scene ("dansen in de keuken om zeven uur 's avonds").
7. CALL-TO-ACTION met codewoord: "Reageer met [WOORD], dan stuur ik je mijn verhaal. Liever privé? Stuur me een berichtje." Zacht, niet verkoperig. Het codewoord is één woord in kapitalen dat bij het verhaal past.
`.trim();

export const HOOK_TYPES = `
HOOK-TYPES (kies wat bij het verhaal past, eis is altijd: specifiek, nooit generiek):
- Overtuiging-kantelend: een aanname die iedereen gelooft, onderuit ("dat hoort erbij als je ouder wordt").
- Kwetsbaar: iets toegeven wat mensen zelden hardop zeggen ("Ik heb dit bericht drie keer verwijderd voor ik het durfde te plaatsen.").
- Verrassend detail: een klein concreet feit dat vragen oproept ("Mijn wekker staat een uur vroeger en ik word er vrolijker van.").
LET OP (verschil met de Amerikaanse voorbeelden): geen medische inzet, geen ziekte-verhalen, geen doods-angst. Onze stopkracht komt uit emotionele specificiteit uit het dagelijks leven.
`.trim();

export const CTA_REGELS = `
CALL-TO-ACTION-REGELS:
- Primair: codewoord in de comments ("Reageer met WAKKER"). Laagste drempel, voedt het algoritme, en wie reageert geeft toestemming voor een berichtje.
- Secundair, altijd erbij: "liever privé? Stuur me een berichtje."
- Nooit: link in de post, productnaam, "koop", "schrijf je in", urgentie-trucs ("nog 3 plekken").
- Bij de opvolging na een reactie geldt de vaste toon: helemaal vrijblijvend, als het niets voor je is is dat ook prima.
`.trim();

export const PRE_POST_STRUCTUUR = `
PRE-POST (vóór de start van een 21-daags traject), 4 delen, open en eerlijk, GEEN tease:
1. Korte emotionele opener (scroll-stop).
2. Hoe het voelde vóór de start (gevoelstaal, zintuiglijk).
3. Wat diegene heeft BESLOTEN te gaan doen (leefstijl-taal: gezonder eten, meer water, beter slapen, rustmomenten; met begeleiding van iemand die het zelf deed).
4. Positieve verwachting + "ik neem jullie mee, ook als het tegenzit" + bedankje aan de begeleider + vraag aan de lezer ("Duim je mee?").
`.trim();

export const RESULTAAT_POST_STRUCTUUR = `
21-DAGEN-RESULTATEN-POST (na afloop), 5 delen, staat op zichzelf:
1. Scroll-stop opener.
2. Hoe het vóór de start was.
3. Wat diegene heeft veranderd tijdens het traject (gedrag).
4. Wat het heeft opgeleverd: gevoel, energie, balans, trots (claim-vrij, concreet, het liefst als scene).
5. Dankbaarheid + codewoord-CTA.
`.trim();

export const LANCEERWEEK_BOGEN = `
DE ELEVA-LANCEERWEEK (6 dagen, warme markt, één post per dag). Drie instromen:

INSTROOM 1, starter MET eigen resultaat:
- Dag 1 Aanzetje: vraag-post in de niche (kleurenblok, één vraag, geen foto). Doel: reacties, algoritme wakker.
- Dag 2 Ik neem je mee: open vertellen dat je een tijd geleden iets bent gaan veranderen en er deze week over gaat delen, eerlijk, ook wat tegenviel. GEEN tease.
- Dag 3 Jouw verhaal: de volledige story-post (7 beats, codewoord-CTA). Dit is de belangrijkste post van de week.
- Dag 4 Dankjewel: reacties benoemen en bedanken, zonder pitch.
- Dag 5 Ander medium: hetzelfde verhaal als video/live/reel (of B-roll met tekst voor wie niet wil praten).
- Dag 6 Open en eerlijk: de twijfel-post, wat je bijna had laten stoppen.
- Dag 7-10 optioneel, business-boog: zelfde structuur over het BOUWEN (extra inkomstenstroom-taal, geen bedragen).

INSTROOM 2, starter ZONDER eigen resultaat:
- Dag 1 Aanzetje (zelfde als boven).
- Dag 2 PRE-POST (de bestaande 4-delen-structuur): vandaag start ik, ik neem jullie mee.
- Dag 3 Waarom nu: de worsteling die de aanleiding was (verlangen, geen resultaat-claims).
- Dag 4 Dankjewel + wat de eerste dagen opvallen (ervaring, geen claims).
- Dag 5 Ander medium: meenemen in je dag of ritueel.
- Dag 6 Open en eerlijk.
- FINALE op dag 21 van het programma: de 21-dagen-resultaten-post (5 delen) als kroon.

INSTROOM 3, doorbouwer (heeft eerder al pre-post en resultaten-post gedaan, GEEN herstart):
- Dag 1 Aanzetje.
- Dag 2 Waar ik nu sta: update sinds de eerdere posts, wat er onderweg veranderde.
- Dag 3 Het verhaal van een ander: de eerste klant of iemand die je begeleidde (met toestemming, claim-vrij, geen naam nodig).
- Dag 4 Dankjewel.
- Dag 5 Ander medium of nieuwe invalshoek (andere niche mag).
- Dag 6 Open en eerlijk over het bouwen: waarom je dit doet, extra inkomstenstroom-taal.

WEEKRITME NA DE LANCEERWEEK (rotatie, 15 min per dag): ma aanzetje-vraag, di verhaal van een klant, wo iets leren/waarde, do lifestyle-inkijkje, vr persoonlijk verhaal, za video/reel.
`.trim();

export const REELS_FRAMEWORK = `
REELS (voor wie niet wil praten op camera, B-roll + tekst werkt bewezen):
- Video: gewoon beeld uit je dag (B-roll) met tekst-overlays: 1) aandacht-hook gericht op de doelgroep, 2) verdieping van het probleem in ik-vorm, 3) "lees de caption" als afsluiter.
- Caption in 7 delen: tweede hook (zichtbaar vóór "meer lezen"), mini-transformatieverhaal in 1-3 zinnen, harde waarheid ("ik dacht dat het X was... het bleek Y"), 3-5 concrete tips die op zichzelf waardevol zijn (zo goed dat mensen 'm opslaan), waarom alleen-zelf-uitzoeken meer tijd en gedoe kost, de brug ("wat mij hielp", zonder naam), codewoord-CTA.
- Product en bedrijf nooit in beeld en nooit bij naam.
`.trim();

/** Alle copywriting-kennis in één blok, voor injectie bij post/reel-taken. */
export function bouwCopywritingSectie(): string {
  return [
    "=== COPYWRITING-MOTOR (gebruik deze opbouw, dit is de geteste structuur) ===",
    "LET OP: alle voorbeelden in deze instructies ('dansen in de keuken', 'drie koffie per dag', 'elke middag rond drie uur') zijn ILLUSTRATIE van het soort detail. Neem ze NOOIT over in een echte post: elk detail komt van de gebruiker zelf, anders posten meerdere teamleden hetzelfde verzonnen leven.",
    STORY_POST_FRAMEWORK,
    "",
    HOOK_TYPES,
    "",
    CTA_REGELS,
    "",
    PRE_POST_STRUCTUUR,
    "",
    RESULTAAT_POST_STRUCTUUR,
    "",
    REELS_FRAMEWORK,
    "",
    LANCEERWEEK_BOGEN,
  ].join("\n");
}
