export const STORY_POST_FRAMEWORK = `
STORY-POST-OPBOUW (7 stappen, in deze volgorde, als doorlopend verhaal zonder kopjes):
1. HOOK: overtuiging-kantelende openingszin, kwetsbaar of verrassend. Nooit "wauw wat een verschil" (te tam, dat post iedereen al). Wel: iets wat een aanname omverduwt ("Iedereen zei: dat hoort erbij. Ik heb het bijna geloofd.").
2. WORSTELING: hoe het was, met minstens twee ZINTUIGLIJKE details (tijdstip, plek, een concreet moment: "elke middag rond drie uur", "op het verjaardagsfeestje zat ik erbij maar was ik er niet").
3. ANGST: wat er echt op het spel stond, de gedachte die 's nachts wakker hield. Menselijk, geen drama om het drama.
4. ONTDEKKING: hoe diegene het antwoord vond, via een mens, niet via een product. Sla dit niet over, dit is het herkenbare deel.
5. HARDE WAARHEID + DE PRODUCT-BRUG: "ik dacht dat het aan discipline lag... wat voor mij het verschil maakte was een compleet programma dat alles combineerde." Hier moet de lezer horen dat er iets CONCREETS achter zat: een programma, een traject, een complete aanpak met begeleiding, inclusief iets feitelijks als "ook met wat ik elke ochtend neem". Geen naam, geen merk, geen medische werking. Maar laat het verschil NOOIT op alleen leefstijl of wilskracht lijken als er een programma achter zit: dat is oneerlijk over wat er echt gebeurde, en "gewoon gezonder gaan eten" maakt niemand nieuwsgierig, dan is er niets om het codewoord voor te sturen. Combineer met "zelf proberen werkte niet": wat diegene al jaren alleen probeerde en wat pas lukte mét het plan.
6. RESULTAAT + DROOM: concreet voor-en-na in gevoel en gedrag (claim-vrij), plus wat nu kan wat eerst niet kon, het liefst als scene ("dansen in de keuken om zeven uur 's avonds").
7. CALL-TO-ACTION met codewoord: "Wil je weten wat ik precies heb aangepast? Reageer met [WOORD]. Liever privé? Stuur me een berichtje." Zacht, niet verkoperig. Het codewoord is één woord in kapitalen dat bij het verhaal past en ontgrendelt het HOE (zie CTA-regels).
`.trim();

export const HOOK_TYPES = `
HOOK-TYPES (kies wat bij het verhaal past, eis is altijd: specifiek, nooit generiek):
- Overtuiging-kantelend: een aanname die iedereen gelooft, onderuit ("dat hoort erbij als je ouder wordt").
- Kwetsbaar: iets toegeven wat mensen zelden hardop zeggen ("Ik heb dit bericht drie keer verwijderd voor ik het durfde te plaatsen.").
- Verrassend detail: een klein concreet feit dat vragen oproept ("Mijn wekker staat een uur vroeger en ik word er vrolijker van.").
LET OP (verschil met de Amerikaanse voorbeelden): geen medische inzet, geen ziekte-verhalen, geen doods-angst. Onze stopkracht komt uit emotionele specificiteit uit het dagelijks leven.
`.trim();

export const CTA_REGELS = `
CALL-TO-ACTION-REGELS:
- Primair: codewoord in de comments ("Reageer met WAKKER"). Laagste drempel, voedt het algoritme, en wie reageert geeft toestemming voor een berichtje.
- Het codewoord ontgrendelt het HOE, nooit "mijn verhaal" (het verhaal staat al in de post). Goed: "Wil je weten wat ik precies heb aangepast? Reageer met WAKKER." Fout: "Reageer en ik stuur je mijn verhaal toe."
- Secundair, altijd erbij: "liever privé? Stuur me een berichtje."
- Nooit: link in de post, productnaam, "koop", "schrijf je in", urgentie-trucs ("nog 3 plekken").
- Bij de opvolging na een reactie geldt de vaste toon: helemaal vrijblijvend, als het niets voor je is is dat ook prima.
`.trim();

export const TOPCOPY_LESSEN = `
TOP-COPYWRITER-LESSEN (dit scheidt een 7 van een 9):
- DOOD ELK CLICHÉ: "reddende engel", "wereld van verschil", "wat een verschil!", "nieuw hoofdstuk", "de oude ik". Als je het beeld al duizend keer gelezen hebt, schrap het. Het echte detail van de gebruiker is altijd sterker.
- GEEN META-AANKONDIGINGEN: "voordat ik aan mijn verandering begon", "ik wil mijn verhaal delen". Stap midden in de scène, vertel het verhaal in plaats van het aan te kondigen.
- LATEN ZIEN, NIET BEOORDELEN: geen superlatieven over je eigen verhaal ("echt bizar!", "ongelooflijk"). Het detail doet het werk; de lezer trekt zelf de conclusie.
- ÉÉN VERHAAL, ÉÉN EMOTIE: bouw de hele post rond het STERKSTE ene detail van de gebruiker en laat de rest weg. Alles erin proppen maakt de post vlak.
- SPREEKTAAL-RITME: lees 'm hardop in je hoofd. Elke zin die je nooit tegen een vriendin zou zeggen, herschrijven.
`.trim();

